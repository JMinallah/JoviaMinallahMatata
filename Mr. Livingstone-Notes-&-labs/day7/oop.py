# What is OOP?
# OOP stands for Object-Oriented Programming. 
# It is a programming paradigm that uses objects and classes to organize and structure code.
# A class is a blueprint for creating objects, which are instances of the class.
# An object is an instance of a class.
# Components of  a class include attributes (data members) and methods (functions).

# Syntax of a class:
# class ClassName:
#     def __init__(self, attribute1, attribute2):
#         self.attribute1 = attribute1
#         self.attribute2 = attribute2

# Syntax of creating an object:
# object_name = ClassName(attribute1_value, attribute2_value)

# Syntax of a method:
# def method_name(self, parameter1, parameter2):

# Accessing attributes of an object:
# object_name.attribute_name

# Use of the __init_ method:
# The __init__ method is a special method in Python classes. 
# It is called when an object is created from the class and allows the class to initialize the attributes of the object.

# Use of the self parameter:
# The self parameter is a reference to the current instance of the class.
