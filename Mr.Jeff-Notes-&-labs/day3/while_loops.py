#what is a while loop?
#A while loop is a control flow statement that allows code to be executed repeatedly 
# based on a given boolean condition. 
# The code inside the while loop will continue to execute as long as the condition is true. 
# Once the condition becomes false, the loop will stop executing and the program will continue with the next line of code after the loop.

#syntax of a while loop
# while condition:
#     # code to execute 

#example of a while loop
count = 0
while count < 5:
    print("Count:", count)
    count += 1
    
