# Concepts of OOP in python
# These are the four main concepts of OOP in python
# 1. Encapsulation
# 2. Inheritance
# 3. Polymorphism
# 4. Abstraction

# Encapsulation is the process of wrapping data and methods that work on that data within a single unit. 
# In Python, encapsulation is achieved using classes. It helps to protect the data from being accessed directly from outside the class.

# Inheritance is the process by which one class can inherit the properties and methods of another class.
# Polymorphism is the ability of an object to take on many forms. 
# In Python, polymorphism is achieved through method overriding and operator overloading.
# Abstraction is the process of hiding the implementation details of an object and showing only the essential features of the object.