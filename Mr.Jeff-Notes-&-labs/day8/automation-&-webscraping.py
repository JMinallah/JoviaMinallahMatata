# What is automation?
# Automation refers to the use of technology to perform tasks with minimal human intervention. 
# It involves creating systems or processes that can operate automatically, often using software, scripts, or machines to carry out repetitive or complex tasks efficiently and accurately.

'''
Automation pipeline:
Input: Data or tasks that need to be processed or executed.
Processing: The automation system processes the input data or tasks according to predefined rules, algorithms, or workflows. This may involve data manipulation, calculations, decision-making, or other operations.
Output: The results or outcomes generated by the automation system after processing the input data or tasks. This could be in the form of reports, notifications, updated databases, or any other relevant output.
Feedback Loop: In some automation systems, there may be a feedback loop where the output is analyzed and used to improve or optimize the automation process. This feedback can help refine the system's performance over time.

'''

